class Solution:
    """
    @param A: a list of integers
    @return an integer
    """
    def removeDuplicates(self, A):
        # write your code here
        if len(A)<2:
            return A

        start =0
        for i in range(len(A)-1):
             if A[i]!=A[start]:
                start+=1
                if i!= start:
                    A[start]=A[i]
        return start+1

